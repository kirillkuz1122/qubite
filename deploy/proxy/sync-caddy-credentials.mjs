#!/usr/bin/env node

import fs from "node:fs";
import http from "node:http";
import https from "node:https";
import crypto from "node:crypto";

const apiUrl =
  process.env.QUBITE_PROXY_SYNC_URL ||
  "http://127.0.0.1:3000/api/proxy/sync/credentials";
const syncToken = process.env.PROXY_SYNC_TOKEN || "";
const outputPath =
  process.env.CADDY_FORWARDPROXY_CREDENTIALS ||
  "/etc/caddy/forwardproxy-credentials.caddy";
const sniRoutesOutputPath =
  process.env.CADDY_SNI_ROUTES_OUTPUT ||
  "/etc/caddy/qubite-sni-routes.caddy";
const realityConfigPath =
  process.env.SINGBOX_REALITY_CONFIG ||
  "/etc/singbox/config.json";
const nginxStreamConfPath =
  process.env.NGINX_STREAM_CONF ||
  "/etc/nginx/stream.d/sni-router.conf";
const realityPrivateKey = process.env.REALITY_PRIVATE_KEY || "";
const realityShortId = process.env.REALITY_SHORT_ID || "deadbeef";
const realityBasePort = Number(process.env.REALITY_BASE_PORT || 18444);
const caddyPort = Number(process.env.CADDY_HTTPS_PORT || 18443);

if (!syncToken) {
  console.error("PROXY_SYNC_TOKEN is required.");
  process.exit(1);
}

function fetchJson(url) {
    return new Promise((resolve, reject) => {
    const client = url.startsWith("https:") ? https : http;
    const request = client.get(
      url,
      {
        headers: {
          Authorization: `Bearer ${syncToken}`,
          Accept: "application/json",
        },
      },
      (response) => {
        let body = "";
        response.setEncoding("utf8");
        response.on("data", (chunk) => {
          body += chunk;
        });
        response.on("end", () => {
          if (response.statusCode < 200 || response.statusCode >= 300) {
            reject(new Error(`Sync failed: ${response.statusCode} ${body}`));
            return;
          }
          resolve(JSON.parse(body));
        });
      },
    );
    request.on("error", reject);
    request.setTimeout(5000, () => {
      request.destroy(new Error("Sync timed out."));
    });
  });
}

function quote(value) {
  return JSON.stringify(String(value || ""));
}

function isSafeDomain(value) {
  return /^[a-z0-9.-]+$/.test(String(value || "")) && String(value || "").includes(".");
}

function normalizeRedirectUrl(value) {
  try {
    const url = new URL(String(value || ""));
    if (!["http:", "https:"].includes(url.protocol)) {
      return "";
    }
    url.hash = "";
    return url.toString().replace(/\/$/, "");
  } catch (error) {
    return "";
  }
}

function writeAtomic(filePath, content, mode = 0o600) {
  const temporaryPath = `${filePath}.tmp`;
  fs.writeFileSync(temporaryPath, content, { mode });
  fs.renameSync(temporaryPath, filePath);
}

const payload = await fetchJson(apiUrl);
const credentials = Array.isArray(payload.credentials)
  ? payload.credentials
  : [];
const sniRoutes = Array.isArray(payload.sniRoutes) ? payload.sniRoutes : [];

const lines = [
  "# Generated by Qubite proxy sync. Do not edit manually.",
  `# Generated at: ${new Date().toISOString()}`,
];

if (credentials.length === 0) {
  lines.push("basic_auth placeholder placeholder-change-me");
} else {
  for (const credential of credentials) {
    lines.push(
      `basic_auth ${quote(credential.username)} ${quote(credential.password)}`,
    );
  }
}

writeAtomic(outputPath, `${lines.join("\n")}\n`);

const routeLines = [
  "# Generated by Qubite proxy sync. Do not edit manually.",
  `# Generated at: ${new Date().toISOString()}`,
];

for (const route of sniRoutes) {
  const domain = String(route.domain || "").trim().toLowerCase();
  const redirectUrl = normalizeRedirectUrl(route.redirectUrl);
  if (!isSafeDomain(domain) || !redirectUrl) {
    continue;
  }
  routeLines.push(
    "",
    `https://${domain} {`,
    "    route {",
    "        forward_proxy {",
    "            import /etc/caddy/forwardproxy-credentials.caddy",
    "            hide_ip",
    "            hide_via",
    "            probe_resistance",
    "        }",
    "",
    `        redir ${redirectUrl}{uri} 302`,
    "    }",
    "}",
  );
}

writeAtomic(sniRoutesOutputPath, `${routeLines.join("\n")}\n`);

// --- sing-box Reality config (multi-SNI inbounds) ---

const VLESS_UUID_NAMESPACE = "6ba7b810-9dad-11d1-80b4-00c04fd430c8";

function deriveVlessUuid(subscriptionSecret) {
  const hash = crypto.createHash("sha1")
    .update(VLESS_UUID_NAMESPACE.replace(/-/g, ""), "hex")
    .update(String(subscriptionSecret))
    .digest("hex");
  return [
    hash.slice(0, 8),
    hash.slice(8, 12),
    "5" + hash.slice(13, 16),
    ((parseInt(hash.slice(16, 18), 16) & 0x3f) | 0x80).toString(16).padStart(2, "0") + hash.slice(18, 20),
    hash.slice(20, 32),
  ].join("-");
}

const realityUsers = Array.isArray(payload.realityUsers)
  ? payload.realityUsers
  : [];

// Collect unique target SNIs from routes
const uniqueSniTargets = [...new Set(
  sniRoutes
    .map((r) => String(r.targetSni || "").trim().toLowerCase())
    .filter((s) => s && s.includes("."))
)].sort();

if (realityPrivateKey && realityUsers.length > 0 && uniqueSniTargets.length > 0) {
  const users = realityUsers
    .filter((u) => u.uuid)
    .map((u) => ({
      uuid: u.uuid,
      flow: "xtls-rprx-vision",
    }));

  // One inbound per target SNI, each on its own port
  const inbounds = uniqueSniTargets.map((targetSni, index) => ({
    type: "vless",
    tag: `vless-reality-${targetSni.replace(/[^a-z0-9]/g, "-")}`,
    listen: "127.0.0.1",
    listen_port: realityBasePort + index,
    users,
    tls: {
      enabled: true,
      server_name: targetSni,
      reality: {
        enabled: true,
        handshake: {
          server: targetSni,
          server_port: 443,
        },
        private_key: realityPrivateKey,
        short_id: [realityShortId],
      },
    },
  }));

  const singboxConfig = {
    log: {
      level: "info",
      output: "/var/log/singbox/reality.log",
      timestamp: true,
    },
    inbounds,
    outbounds: [
      { type: "direct", tag: "direct" },
    ],
  };

  const configDir = realityConfigPath.replace(/\/[^/]+$/, "");
  if (!fs.existsSync(configDir)) {
    fs.mkdirSync(configDir, { recursive: true, mode: 0o700 });
  }
  writeAtomic(realityConfigPath, JSON.stringify(singboxConfig, null, 2) + "\n");
  console.log(`Reality config written: ${users.length} user(s), ${inbounds.length} SNI inbound(s): ${uniqueSniTargets.join(", ")}`);
} else if (!realityPrivateKey) {
  console.log("REALITY_PRIVATE_KEY not set, skipping sing-box config.");
} else if (uniqueSniTargets.length === 0) {
  console.log("No SNI routes configured, skipping sing-box config.");
} else {
  console.log("No Reality users received, skipping sing-box config.");
}

// --- nginx stream SNI router config ---

const nginxSniMapEntries = uniqueSniTargets.map((sni, index) => {
  const port = realityBasePort + index;
  return `        ${sni.padEnd(40)} reality_${port};`;
});

const nginxUpstreamBlocks = uniqueSniTargets.map((sni, index) => {
  const port = realityBasePort + index;
  return `    upstream reality_${port} {\n        server 127.0.0.1:${port};\n    }`;
});

const nginxStreamConf = `# Generated by Qubite proxy sync. Do not edit manually.
# Generated at: ${new Date().toISOString()}
# SNI targets: ${uniqueSniTargets.join(", ") || "none"}

stream {
    log_format stream_basic '$remote_addr [$time_local] '
                            '$protocol $status $bytes_sent $bytes_received '
                            '$session_time "$ssl_preread_server_name" -> $upstream_addr';
    access_log /var/log/nginx/stream-access.log stream_basic;

    map $ssl_preread_server_name $backend {
        ~\\.qubiteapp\\.online$          caddy_backend;
        ~\\.qubiteapp\\.ru$              caddy_backend;
        qubiteapp.online                 caddy_backend;
        qubiteapp.ru                     caddy_backend;
        ""                               caddy_backend;
${nginxSniMapEntries.join("\n")}
        default                          caddy_backend;
    }

    upstream caddy_backend {
        server 127.0.0.1:${caddyPort};
    }

${nginxUpstreamBlocks.join("\n\n")}

    server {
        listen 443;
        listen [::]:443;
        ssl_preread on;
        proxy_pass $backend;
        proxy_protocol off;
        proxy_connect_timeout 5s;
        proxy_timeout 300s;
    }
}
`;

const nginxStreamDir = nginxStreamConfPath.replace(/\/[^/]+$/, "");
if (!fs.existsSync(nginxStreamDir)) {
  fs.mkdirSync(nginxStreamDir, { recursive: true });
}
writeAtomic(nginxStreamConfPath, nginxStreamConf, 0o644);
console.log(`nginx stream conf written: ${uniqueSniTargets.length} SNI route(s).`);
