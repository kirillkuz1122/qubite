#!/usr/bin/env node

import fs from "node:fs";
import http from "node:http";
import https from "node:https";

const apiUrl =
  process.env.QUBITE_PROXY_SYNC_URL ||
  "http://127.0.0.1:3000/api/proxy/sync/credentials";
const syncToken = process.env.PROXY_SYNC_TOKEN || "";
const outputPath =
  process.env.CADDY_FORWARDPROXY_CREDENTIALS ||
  "/etc/caddy/forwardproxy-credentials.caddy";

if (!syncToken) {
  console.error("PROXY_SYNC_TOKEN is required.");
  process.exit(1);
}

function fetchJson(url) {
    return new Promise((resolve, reject) => {
    const client = url.startsWith("https:") ? https : http;
    const request = client.get(
      url,
      {
        headers: {
          Authorization: `Bearer ${syncToken}`,
          Accept: "application/json",
        },
      },
      (response) => {
        let body = "";
        response.setEncoding("utf8");
        response.on("data", (chunk) => {
          body += chunk;
        });
        response.on("end", () => {
          if (response.statusCode < 200 || response.statusCode >= 300) {
            reject(new Error(`Sync failed: ${response.statusCode} ${body}`));
            return;
          }
          resolve(JSON.parse(body));
        });
      },
    );
    request.on("error", reject);
    request.setTimeout(5000, () => {
      request.destroy(new Error("Sync timed out."));
    });
  });
}

function quote(value) {
  return JSON.stringify(String(value || ""));
}

const payload = await fetchJson(apiUrl);
const credentials = Array.isArray(payload.credentials)
  ? payload.credentials
  : [];

const lines = [
  "# Generated by Qubite proxy sync. Do not edit manually.",
  `# Generated at: ${new Date().toISOString()}`,
];

if (credentials.length === 0) {
  lines.push("basic_auth placeholder placeholder-change-me");
} else {
  for (const credential of credentials) {
    lines.push(
      `basic_auth ${quote(credential.username)} ${quote(credential.password)}`,
    );
  }
}

const temporaryPath = `${outputPath}.tmp`;
fs.writeFileSync(temporaryPath, `${lines.join("\n")}\n`, {
  mode: 0o600,
});
fs.renameSync(temporaryPath, outputPath);
